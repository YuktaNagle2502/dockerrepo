# slicing - [start argument: stop argument -1]
language = "python"
print(language[1:4]) # last index is not included in slicing
print(language[1:]) # from index 1 to end of string
print(language[:4]) # from start to index 4
print(language[-2:6]) 
print(language[:])  # to print the whole string
print(language[:-1])